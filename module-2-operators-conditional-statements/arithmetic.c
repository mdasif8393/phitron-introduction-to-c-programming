#include<stdio.h>

int main(){

    int a = 10;
    int b = 5;

    int sum = a + b;
    printf("Summation is: %d\n", sum);

    int sub = a - b;
    printf("Subtraction is: %d\n", sub);

    int mul = a * b;
    printf("Multiplication is: %d\n", mul);

    int div = a / b;
    printf("Division is: %d\n", div);

    return 0;
}